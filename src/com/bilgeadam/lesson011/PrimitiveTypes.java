package com.bilgeadam.lesson011;

public class PrimitiveTypes {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int c = 30;

		System.out.println("a: " + a + " b: " + b + " c: " +  c);
		
		a = c;
		System.out.println("a: " + a + " b: " + b + " c: " +  c);
		
		a = 15;
		System.out.println("a: " + a + " b: " + b + " c: " +  c);
	}

}
